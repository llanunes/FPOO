class Radiciacao {
    public static void main (String args []) {
        int numero1 = 81;
        int numero2 = 25;

        double raiz1 = Math.sqrt(numero1);
        double raiz2 = Math.sqrt(numero2);

        System.out.println("Primeira raiz:" + raiz1);
        System.out.println("Segunda raiz:"+ raiz2);
    }
}  


 